pub fn mouse_button_code_convert(rdev_button: Button) -> u32 {
    match rdev_button {
        Button::Left => 0,
        Button::Middle => 2,
        Button::Right => 1,
        Button::Unknown(code) => match code {
            8 | 19 => 3,
            9 | 20 => 4,
            _ => panic!("Unknown mouse button code: {:?}", rdev_button),
        },
    }
}

